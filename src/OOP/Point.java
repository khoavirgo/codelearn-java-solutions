package OOP;

public class Point {
    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public double distance(Point another) {
        double dx = another.x - x;
        double dy = another.y - y;
        return Math.sqrt(dx * dx + dy * dy);
    }
}
